N = 1000;
C = 100;
P = 1200;

f = @(x) C./(1+x) + C./(1+x).^2 + C./(1+x).^3 + (C+N)./(1+x).^4 - P;
df = @(x) -C./(1+x).^2 -2*C./(1+x).^3 -3*C./(1+x).^4 - 4*(C+N)./(1+x).^5;

%%% 1 %%%
[xb,Xb] = bisekcija(f,0,1,10);

%%% 2 %%%
[xt,Xt] = tangentna_koraki(f,df,0,3);

%%% 3 %%%
[xs,Xs,ks] = sekantna(f,0,1,1e-4,100);
